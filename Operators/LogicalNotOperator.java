package Operators;

public class LogicalNotOperator {
	 public static void main(String[] args)
	    {
	       
	        int a = 10, b = 1;
	 
	       
	        System.out.println("Var1 = " + a);
	        System.out.println("Var2 = " + b);
	 
	       
	        System.out.println("!(a < b) = " + !(a < b));
	        System.out.println("!(a > b) = " + !(a > b));
	    }

}
